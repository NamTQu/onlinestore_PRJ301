package model;

/**
 *
 * @author Admin
 */
public class total {
    private double price;
    private int quantity;

    public total() {
    }

    public total(double price, int quantity) {
        this.price = price;
        this.quantity = quantity;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    @Override
    public String toString() {
        return "total{" + "price=" + price + ", quantity=" + quantity + '}';
    }
    
    
}
