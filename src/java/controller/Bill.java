    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession httpSession = request.getSession();
        if (httpSession.getAttribute("role") == null) {
            request.getRequestDispatcher("view/login.jsp").forward(request, response);
        } else {
            orderDAO orderD = new orderDAO();
            String account = (String) httpSession.getAttribute("username");
            ArrayList<order> orders = orderD.getOrder(account);
            ArrayList<bill> bills = new ArrayList<>();
            for (order order1 : orders) {
                ArrayList<product_ordered> ordered = orderD.getOrderedProduct(order1.getId());
                bills.add(new bill(order1.getName(), order1.getPhone_number(), order1.getAddress(), order1.getOrder_date(), order1.getTotal_price(), ordered));
            }
            request.setAttribute("bills", bills);
            request.getRequestDispatcher("view/bill.jsp").forward(request, response);
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        processRequest(request, response);
    }

